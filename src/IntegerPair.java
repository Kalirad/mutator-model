public class IntegerPair {
    private int intA;
    private int intB;

    public IntegerPair(int intA, int intB) {
        this.intA = intA;
        this.intB = intB;
    }

    public int getIntA() {
        return intA;
    }

    public int getIntB() {
        return intB;
    }

}
